											<p style="text-align: justify;">Beberapa kebijakan pemerintah pusat akan berpengaruh langsung pada semakin banyaknya pendatang memasuki wilayah Indonesia. Pemberlakuan Masyarakat Ekonomi Asean (MEA), kemudahan regulasi bidang investasi, dan pemberlakuan kebijakan 169 negara bebas visa bagi wisatawan, akan menyebabkan Indonesia akan kebanjiran pendatang, baik sebagai wisatawan, pebisnis, pedagang maupun pemukim. Dengan demikian, tatanan kehidupan sosial yang berciri plural akan menjadi keniscayaan. </p>
											<p style="text-align: justify;">Sejatinya Pluralitas adalah suatu kodrat yang tidak mungkin dihilangkan. Bangsa Indonesia dikenal antara lain karena keragamannya dalam banyak hal seperti agama, suku, bahasa dan budaya. Provinsi Nusa Tenggara Barat (NTB) merupakan salah satu provinsi di Indonesia yang juga memiliki karakter yang plural. Di samping didiami oleh tiga suku dominan (Sasak, Samawa, dan Mbojo), provinsi ini juga memiliki sejarah panjang hidup berdampingan dengan seluruh suku yang ada di Indonesia, dengan beragam agama dan budaya-nya masing-masing.</p>
											<p style="text-align: justify;">Pluralitas yang dikelola dan disikapi secara tidak bijak, akan membawa dampak negative yang banyak bagi kehidupan masyarakat. NTB memiliki pengalaman buruk tentang ketidak mampuan warganya menyikapi perbedaan. Kasus “kerusuhan 171” yang terjadi pada 17 Januari 2000 dan kasus kerusuhan Sumbawa pada tahun 2012 menjadi dua contoh betapa rapuhnya pemahaman pentingnya pluralitas dari warga NTB. Konflik sosial yang terjadi pada dasarnya dipicu oleh hal-hal yang sepele. Beberapa kasus kekerasan dan kerusuhan di NTB menunjukkan masih belum adanya kesadaran dari warga untuk hidup akur, mentaati hukum, dan menyelesaikan persoalan secara kekeluargaan dan penegakan hukum.</p>
											<p style="text-align: justify;">Untuk mendorong dan menciptakan tantanan kehidupan sosial, budaya, dan politik masyarakat yang baik di NTB, tidak cukup hanya dilakukan lewat ceramah, penyuluhan, dan hal-hal yang bersifat oral lainnya, tetapi dperlukan upayakan nyata dan konkrit dalam menciptakan dan membangun suatu kemunitas secara langsung, dengan kegiatan-kegiatan riil yang dibangun atas dasar kesadaran bersama di antara komunitas. Komitmen yang dibangun diharapkan menjadi contoh bagi tatanan kehidupan yang inklusif, toleran, multicultural, penegakan hukum dan hal-hal baik lainnya.</p>
											<p style="text-align: justify;">Potret komunitas idaman seperti ini dinamakan dengan “Desa Madani”. Desa Madani seperti ini akan dibangun dengan memilih 4 (empat) desa sebagai pilot – project. Setelah berhasil maka akan diaplikasikan di desa – desa lain di NTB. Dengan demikian program Desa Madani ini merapikan langkah nyata di dalam mengimplementasikan visi NTB yang beriman, berbudaya, berdaya saing, dan sejahtera.</p>


											<h4 class="has-text-align-center has-vivid-cyan-blue-background-color has-background wp-block-heading">TUJUAN</h4>


											<p style="text-align: justify;">Secara umum, program pengembangan “Desa Madani” ini bertujuan untuk menciptakan kawasan/desa madani yang dijadikan sebagai laboratorium sosial Kesbangpoldagri NTB dan dapat dijadikan sebagai percontohan bagi daerah/desa lainnya.</p>


											<h4 class="has-text-align-center has-vivid-cyan-blue-background-color has-background wp-block-heading"><strong>LOKASI PELAKSANAAN KEGIATAN</strong></h4>


											<ol>
												<li style="text-align: justify;">Desa Doropeti, Kecamatan Pekat, Kabupatrn Dompu, atas alasan karena desa tersebut merupakan rencana lokasi pendirian pabrik gula.</li>
												<li style="text-align: justify;">Desa Bukit Damai, Kecamatan Seteluk, Kabupaten Sumbawa Barat, yang merupakan aktifiitas perusahaan tambang emas dan perak oleh PT. Newmont Nusa Tenggara.</li>
												<li style="text-align: justify;">Desa Kute, Kecamatan Pujut, Kabupaten Lombok Tengah, merupakan salah satu daerah tujuan wisata dan rencana lokasi pembangunan Kawasan Ekomomi Khusus Pariwisata MANDALIKA LOMBOK.</li>
												<li style="text-align: justify;">Desa Sesait, Kecamatan Kayangan, Kabupaten Lombok Utara, yang direncanakan akan dibangun sebuah hub internasional (<em>Global Hub</em>).</li>
											</ol>


											<h4 class="has-text-align-center has-vivid-cyan-blue-background-color has-background wp-block-heading"><strong>KONDISI AWAL</strong></h4>


											<p style="text-align: justify;">Karakter kemajemukan di NTB sudah merambah sampai ke pelosok, sehingga sudah sangat sulit menemukan masyarakat yang secara murni masih homogen. Namun demikian, masyarakat tradisional memiliki mekanisme sosial dalam menyikapi kemajemukan pada masyarakat. Munculnya sebutan-sebutan yang menandakan perubahan sikap terhadap kehadiran ”pendatang” merupakan proses integrasi sosial yang berlangsung secara alami. Pada masyarakat Sasak, misalnya, proses integrasi sosial ini menjelma di dalam sebutan – sebutan bagi pendatang, yaitu sebagai berikut:</p>
											<ul>
												<li style="text-align: justify;">“Tau” – sebutan untuk pendatang yang bercitra negatif</li>
												<li style="text-align: justify;">“Dengan” – sebutan untuk pendatang yang belum berproses secara sosial</li>
												<li style="text-align: justify;">“Batur” – sebutan untuk pendatang yang telah dapat menyesuaikan diri dengan lingkungan baru</li>
												<li style="text-align: justify;">“Semeton” – sebutan untuk pendatang yang telah menyatu dengan masyarakat setempat.</li>
											</ul>


											<h4 class="has-text-align-center has-vivid-cyan-blue-background-color has-background wp-block-heading"><strong>KONDISI SAAT INI</strong></h4>


											<p style="text-align: justify;">Hingga saat ini&nbsp; baru terdapat 2 (dua) desa&nbsp; yang sudah tersusun awiq-awiqnya dari 4 (empat) desa yang menjadi pilot project. Kedua desa tersebut yaitu desa Kute Kecamatan Pujut Kabupaten Lombok Tengah dan desa Sesait kecamatan Kayangan Kabupaten Lombok Utara. Terhadap dua desa tersebut pada 2019 Bakesbangpoldagri peov NTB telah melakukan sosialisasi keberadaan Awiq-awiq kepada masyarakat setempat sebagai aturan yang harus ditaati dan disepakati bersama, sementara untuk dua desa yang belum selesai penyusunan awiq-awiqnya akan dilanjutkan pada 2020.</p>


											<h4 class="has-text-align-center has-vivid-cyan-blue-background-color has-background wp-block-heading"><strong>RINCIAN KEGIATAN</strong></h4>


											<p style="text-align: justify;">Kegiatan yang dilaksanakan adalah:</p>
