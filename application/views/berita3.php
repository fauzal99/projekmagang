                                            <p>Rabu, 13 Desember 2023, Pemerintah Provinsi NTB melalui Bakesbangpoldagri Provinsi NTB melaksanakan satu kegiatan strategis yaitu Pemberdayaan terhadap Organisasi, Organisasi Kemasyarakatan dan Pondok Pesantren melalui Program Sosialisasi Peraturan Perundangan Keormasan dengan tema &#8220;Pembinaan Ormas dan Ponpes Dalam Rangka Menjaga Keutuhan NKRI.<br>Kegiatan yang dibuka oleh Kepala Bakesbangpoldagri Provinsi NTB, H. Ruslan Abdul Gani, Sh.,MH., diadakan di Aula Pondok Pesantren Nurussalam Dusun Reak, Desa Tanak Awu, Kec. Pujut, Kab. Lombok Tengah, dengan 3 orang narasumber yaitu, Kepala Bakesbangpoldagri Provinsi NTB, Kepala Bidang Politik Kesbangpol Lombok Tengah H. Amiruddin, SH. Dan Ketua Bale Media Mediasi NTB Dr. H.L. Sajim Satrawan.<br>Penyajian materi dan Diskusi serta tanya jawab yang di pandu oleh Lalu Ary (lawyer, pengurus Ponpes dan Ketua LSM Kasta Lombok Tengah) berjalan lancar dan tampak para peserta sangat antusias ikuti kegiatan, hal ini terlihat dari kehadiran peserta yang semula diundang 40 orang dan yg hadir sebanyak 65 orang. Juga sangat bersemangat dalam diskusi dan tanya jawab. Banyak ide dan pemikiran menarik dan visioner guna ditindaklajuti ke depannya. Seperti ide dan gagasan dari Pimpinan Ponpes Nurussalam yaitu Pemprov NTB melalui Bakesbangpoldagri menyelenggarakan Sosialisasi Lagu Mars NTB Gemilang di setiap Ponpes dalam rangka menumbuhkembangkan rasa cinta terhadap NTB. Sehingga seluruh termotivasi untuk membangun NTB yang Maju Melaju.</p>


