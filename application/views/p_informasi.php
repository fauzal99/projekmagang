											<p><strong>Langkah 1 </strong>Pemohon Informasi Publik mempersiapkan salinan/&nbsp;<em>photocopy&nbsp;</em>identitas pemohon &amp; pengguna informasi yang terdiri dari :</p>



											<p>1. Individu :<br>&nbsp; &nbsp; – KTP/ SIM/ Paspor<br>2. Kelompok Orang :<br>&nbsp; &nbsp; – KTP/ SIM/ Paspor seluruh anggota kelompok pemohon<br>&nbsp; &nbsp; – Surat kuasa kepada perwakilan kelompok pemohon<br>3. Organisasi Berbadan Hukum :<br>&nbsp; &nbsp; – Lembar Pengesahan Badan Hukum oleh Menteri Hukum dan HAM<br>&nbsp; &nbsp; – KTP/ SIM/ Paspor perwakilan pengurus/ anggota Badan Hukum<br>&nbsp; &nbsp; – AD/ART Organisasi</p>



											<p><strong>Langkah 2</strong> Pemohon Informasi Publik mendatangi&nbsp;<em>desk</em>&nbsp;layanan PPID yang ada di Bagian Umum dan Kepegawaian dan mengisi formulir permohonan informasi.</p>



											<p><strong>Langkah 3</strong> <em>Desk</em>&nbsp;Layanan PPID menerima dan memverifikasi kelengkapan administrasi berkas permohonan.</p>



											<p><strong>Langkah 4 </strong>Apabila berkas permohonan dinyatakan lengkap, maka pemohon akan mendapatkan Nomor Pendaftaran dan Tanda Terima Permohonan.</p>



											<p><strong>Langkah 5</strong> Pemohon menunggu proses permohonan selama maksimal 10 hari kerja. PPID berhak menambah waktu selama maksimal 1 x 7 hari kerja apabila PPID membutuhkan waktu tambahan untuk menyusun keputusan terkait permohonan informasi tersebut dan menyampaikan pemberitahuan kepada pemohon.</p>



											<p><strong>Langkah 6 </strong>Pemohon mendapatkan pemberitahuan mengenai ketersediaan informasi dan mendapatkan surat keputusan PPID terkait informasi yang dimohonkan.</p>



