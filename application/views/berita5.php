                                            <p>Jum’at 8 Desember 2023 Kepala Bakesbangpoldagri Provinsi Nusa Tenggara Barat H. Ruslan Abdul Gani, Sh.,MH. Beserta Tim mendampingi PJ Gubernur, Drs. H. Lalu Gita Ariadi M.Si. dalam Kegiatan Jum’at Salam di Pemenang Lombok Utara yang dirangkaikan dengan Peresmian dan Alih Kelola Terminal Tipe B Bangsal dan Pelabuhan Pemenang.<br>Hadir dalam acara tersebut, Bupati Lombok Utara, Djohan Syamsu, Kepala Dinas Perhubungan, Lalu Fauzal, seluruh Kepala Perangkat Daerah Provinsi NTB dan semua Perangkat Daerah Kabupaten Lombok Utara.<br>Acara dibuka oleh Kepala Dinas Perhubungan Provinsi NTB, yang dilanjutkan dengan arahan oleh PJ Gubernur yang menyampaikan bahwa atensi kita memperbaiki terminal Bangsal dan sebagainya adalah langkah tepat.<br>Acara dilanjutkan dengan penandatangan berita acara serah terima Aset Terminal Tanjung dan Terminal Bangsal Pemenang oleh Bupati Lombok Utara dan Penandatangan Perjanjian Kerjasama antara Dinas Perhubungan Provinsi NTB dengan Koperasi Jasa Angkut Wisnuman.</p>



