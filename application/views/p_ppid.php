											<p style="text-align: justify;">Pejabat Pengelola Informasi dan Dokumentasi (PPID) Bakesbangpoldagri Provinsi NTB adalah pejabat yang bertanggung jawab di bidang penyimpanan, pendokumentasian, penyediaan, dan/atau pelayanan informasi di Badan Bakesbangpoldagri Provinsi NTB. Dengan keberadaan PPID maka masyarakat yang akan menyampaikan permohonan informasi lebih mudah dan tidak berbelit karena dilayani lewat satu pintu.</p>
											<p style="text-align: justify;">Pemerintah Provinsi Nusa Tenggara Barat telah membentuk Pejabat Pengelola Informasi dan Dokumentasi Provinsi Nusa Tenggara Barat sebagaimana tertuang dalam Peraturan Gubernur Nusa Tenggara Barat Nomor 35 Tahun 2012 Tentang Pedoman Pengelolaan Informasi dan Dokumentasi di Lingkungan Pemerintah Provinsi Nusa Tenggara Barat (telah diperbaharui dengan Peraturan Gubernur Nusa Tenggara Barat Nomor 24 Tahun 2018) yang mana mangatur tata kerja, tata cara pengelolaan dan pelayanan informasi dan dokumentasi oleh Pejabat Pengelola Informasi dan Dokumentasi Pemerintah Provinsi Nusa Tenggara Barat.</p>
											<p style="text-align: justify;">Berdasarkan pergub tersebut di atas dan juga untuk memenuhi Undang-undang tentang Keterbukaan Informasi Publik yang mengatur beberapa hal yaitu : (1) hak setiap Orang untuk memperoleh Informasi; (2) kewajiban Badan Publik menyediakan dan melayani permintaan Informasi secara cepat, tepat waktu, biaya ringan/proporsional, dan cara sederhana; (3) pengecualian bersifat ketat dan terbatas; (4) kewajiban Badan Publik untuk membenahi sistem dokumentasi dan pelayanan Informasi. Maka Kepala Bakesbangpoldagri Provinsi NTB menerbitkan SK Nomor : 120.3/778/X/BKBPDN/2020, Tanggal 12 Oktober 2020 tentang Penunjukan Pejabat Pengelola Informasi dan Dokumentasi Pembantu Badan Kesatuan Bangsa dan Politik Dalam Negeri Provinsi Nusa Tenggara Barat Tahun 2020.</p>
