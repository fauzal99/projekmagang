                                            <p> Sejak terbentuk pada tahun 1958 berdasarkan UU No. 64 Tahun 1958 tentang Pembentukan Daerah-daerah Tingkat I Bali, Nusa Tenggara Barat dan Nusa Tenggara Timur, Pemerintah Provinsi NTB secara terus menerus melakukan upaya pembangunan dalam rangka meningkatkan kesejahteraan masyarakatnya. Berbagai terobosan pembangunan yang inovatif dibuat dalam rangka mempercepat laju pertumbuhan kesejahteraan masyarakat yang tertuang dalam satu dokumen perencanaan terintegrasi mulai dari Rencana Pembangunan Jangka Panjang Daerah (RPJPD) dan Rencana Pembangunan Jangka Menengah<br>Daerah (RPJMD).</p>



                                            <p><br>Pada tahun 2014, Pemerintah Provinsi NTB telah menetapkan Peraturan Daerah Provinsi NTB Nomor 2 Tahun 2014 tentang RPJMD NTB Tahun 2013 &#8211; 2018. Dengan lahirnya perda ini, maka seluruh SKPD wajib membuat Rencana Kerja Strategis (Renstra) sebagai tindak lanjut perencanaan dari RPJMD tersebut, termasuk Badan Kesatuan Bangsa dan Politik Dalam Negeri (Bakesbangpoldagri) Provinsi Nusa Tenggara Baratwajib menyusun Renstra Tahun 2013-2018 sebagai acuan koordinasi, sinkronisasi dan kesinambungan proses Perencanaan Pembangunan di Daerah. Selain itu, Renstra Bakesbangpoldagri 2013-2018 dibuat sebagai upaya untuk melanjutkan berbagai capaian pembangunan yang telah dilaksanakan pada periode sebelumnya yang secara keseluruhannya bertujuan untuk mewujudkan Visi dan Misi Bakesbangpoldagri Provinsi NTB yang terkait langsung dengan perwujudan dari visi dan misi Pemerintah Provinsi NTB.<br>Berbagai upaya pembangunan oleh BakesbangpoldagriProvinsi NTB telah menunjukkan hasil yang positif antara lain meningkatnya partisipasi masyarakat dalam politik, terciptanya stabilitas politik, keamanan, ketentraman dan ketertiban masyarakat (kamtrantibmas) secara simultan. Tentu pencapaian ini tidak terlepas dari peran semua pihak baik pemerintah maupun seluruh komponen masyarakat sebagai hasil pelaksanaan tugas pokok dan fungsi Bakesbangpoldagri Provinsi NTB.</p>



                                            <p>Dengan adanya kewenangan menjalankan dan tugas sebagai tanggung jawab, BakesbangpoldagriProvinsi NTB senantiasa melaksanakan berbagai kegiatan dalam rangka menfasilitasi terciptanya kondisi lingkungan yang kondusif hingga memungkinkan seluruh elemen masyarakat dapat menjalankan aktivitasnya secara baik. Oleh karena itu, tolok ukur keberhasilan pelaksanaan tugas pokok dan fungsi BakesbangpoldagriProvinsi NTB dapat dikemukakan sebagai berikut :</p>



                                            <ul>
                                                <li>Makin eksisnya kehidupan kolektif masyarakat warga dalam perbedaan, yang ditandai dengan menurunnya angka kejadian konflik, perkelahian antar kampung, antar golongan dan antar warga masyarakat.</li>
                                            </ul>



                                            <ul>
                                                <li>Makin menurunnya intensitas gangguan ketenteraman dan ketertiban masyarakat yang ditandai dengan makin kurangnya tindakan anarkis dan premanisme yang meresahkan.</li>
                                            </ul>



                                            <ul>
                                                <li>Makin terbangunnyasuasana saling melindungi diantara warga masyarakat baik dalam menghadapi berbagai ancaman bencana alam maupun bencana non alam seperti terorisme, radikalisme, dsb.</li>
                                            </ul>



                                            <p>Eksistensi Bakesbangpoldagri Provinsi NTB dalam melaksanakan tugas dan fungsinya terlihat dari konflik vertikal dan konflik horizontal yang terjadi di Provinsi NTB dari tahun 2010 sampai dengan 2013 menunjukkan adanya penurunan yang cukup signifikan. Pada tahun 2010 terdapat 161 kasus menjadi 97 kasus pada tahun 2011, demikian juga pada tahun 2012 turun menjadi 93 kasus dan pada tahun 2013 turun menjadi 57 kasus. Adapun keberhasilan dalam menekan angka kasus yang terjadi tidak terlepas dari kondisi sebagai berikut:</p>



                                            <ol>
                                                <li>Meningkatnya kenyamanan dan keamanan lingkungan sebagai hasil kegiatan dari Komunitas Intelijen Daerah ( Kominda);</li>
                                                <li>Meningkatkan kemampuan anggota jejaring Kominda kabupaten/kota melakukan deteksi dini dan cegah dini dalam pemeliharaan kantramtibmas dan pencegahan tindak kriminal serta penanganan konflik;</li>
                                                <li>Terciptanya stabilitas daerah yang kondusif melalui kegiatan sbb : </li>
                                            </ol>



                                            <ul>
                                                <li>Rakor stabilitas Polkam menjelang Pemilihan Umum Kepala Daerah (Pemilukada).</li>
                                                <li>Rakor penanganan konflik.</li>
                                                <li>Rapat Kominda.</li>
                                                <li>Rapat pengkajian masalah strategis di kab/kota se NTB melalui kegiatan temu bina pencegahan konflik bagi kalangan mahasiswa/pemuda/ ormas/toma tahun 2013;</li>
                                                <li>Penguatan kapasitas Forum Kewaspadaan Dini Masyarakat (FKDM) melalui kegiatan fasilitasi pembentukan kelembagaan tingkat kabupaten, kecamatan dan lurah/desa, rapat kerja FKDM dan Lokakarya Survei jenis dan sumber konflik se Nusa Tenggara Barat;</li>
                                                <li>Rapat koordinasi antisipasi Gerakan Radikal dan Terorisme di Wilayah NTB;</li>
                                                <li>Penguatan peran FKUB dalam rangka memberikan pencerahan kepada umat beragamamelalui rapat koordinasi anggota FKUB Provinsi NTB;</li>
                                                <li>Penguatan peran FPK melalui rapat koordinasi serta kegiatan fasilitasi pembentukan kelembagaan tingkat Kab./Kota se Provinsi NTB;</li>
                                                <li>Pembentukan Tim terpadu dalam rangka menciptakan stabilitas keamanan dalam negeri sesuai Inpres Nomor 2 Tahun 2013 dan Inpres Nomor 1 Tahun 2014 tentang Penanganan Keamanan Dalam Negeri.</li>
                                            </ul>



