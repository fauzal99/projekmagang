                                            <p style="text-align: justify;">Sekolah Perjumpaan” salah satu ikhtiar untuk menormalisasikan relasi sosial yang model teoretiknya, dibuat generik sehingga bisa diterapkan pada berbagai <em>setting</em>dan juga konteks yang berbeda. Komponen utama dari “Sekolah Perjumpaan”adalah <em>intentional state </em>(kondisi batin) dan <em>languaging </em>yang merupakan modal bersamayang dimiliki oleh setiap orang secara universal. Pembelajaran mengelola <em>intentionalstate </em>dan <em>languaging </em>pada ruang-ruang perjumpaan secara terstruktur dan terukur,akan menjadi sasana pembelajaran bersama hidup elemen-elemen sosial yang salingberterima, terbuka dan toleran. Model sekolah perjumpaan menanamkan nilai-nilaikehidupan yang telah dieksprimentasikan sejak 2013 di 20-an titik yang tersebar dipulau Lombok. Komunitas-komunitas ini yang menjadi laboratorium pembelajaranmasyarakat untuk menciptakan masyarakat sosial yang terbuka, toleran, dan salingberterima. Lokasi-lokasi tersebut tersebar di beberapa titik di Kecamatan Labuapi,Lembar, Peraya, Mantang, Mataram, Gunung Sari 3 (tiga) titik di Mantang, danNarmada.</p>
                                            <p style="text-align: justify;">Sekolah perjumpaan memiliki dua komponen dasar yaitu praktik <em>mental state</em>dan <em>languaging</em>. Dua komponen dasar ini dipraktikkan pada tiga ranah relasi yaitu; 1)relasi subyek-obyek yang melahirkan kemampuan kognitif (<em>cognitive competence</em>), 2)relasi intersubyektif yang melahirkan kemampuan rekognitif (<em>rekognitifcompetence/sosial</em>), dan 3) relasi transubyektif yang melahirkan kemampuan transkognitif/spiritual (<em>trans-recognitif competence</em>). Model sekolah perjumpaan ini, denganpenerapan yang simple dan sederhana bisa memberikan kompetensi holistik padapeserta pebelajar yang pada akhirnya akan menjadi karakter yang hidup dalam setiapsiswa.</p>
                                            <p style="text-align: justify;">Salah satu yang menjadi indikator Kinerja Daerah (IKD) yang terdapat di Badan Kesatuan Bangsa dan Politik Dalam Negeri yaitu Cakupan komunitas sekolah perjumpaan yang terbentuk dalam rangka pembinaan karakter, Jadi selain menjadi program unggulan, Sekolah perjumpaan juga menjadi indikator Kinerja Daerah (IKD).</p>


