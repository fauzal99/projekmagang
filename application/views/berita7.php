                                            <p>Sabtu, tanggal 02 November 2023, bertempat di Kantor Desa Penatoi Kec. Mpunda Kota Bima, telah dilaksanakan kegiatan Jum’at Salam yang dirangkaikan dengan pembukaan oleh Kepala Bakesbangpoldagri&nbsp; Provinsi NTB, H. Ruslan Abdul Gani, SH. MH, menyampaikan maksud dan tujuan dari Pemda Provinsi NTB melaksanakan kegiatan Jum’at Salam, kunjungan Kepala Kesbangpol ke Kelurahan Penatoi tersebut merupakan kunjungan yang kedua setelah Pangdam XI Udayana, karena selama ini masyarakat Kelurahan Penatoi sangat tertutup atau tidak menerima kunjungan dari Pejabat Pemerintah.</p>



                                            <p>Hadir pada kegiatan tersebut Kepala Bakesbangpol. Kota Bima, Lurah Penatoi beserta jajaran, Tim dari Kesbangpol Prov. NTB, Tim dari Kesbangpol Kota Bima dan masyarakat Desa Penatoi.</p>



                                            <p>Rangkaian Kegiatan dimulai dengan&nbsp; Bersilaturrahmi dengan Warga di Kelurahan Penatoi, Tim Bakesbangpol Provinsi NTB, Kesbangpol Kota Bima dan Lurah beserta Staf Kelurahan terjun langsung ke Lokasi Budidaya Keramba Ikan Lele yang di budidayakan oleh masyarakat atas bantuan Program Desa/Kelurahan SIAP SIAGA (Peningkatan Kewaspadaan) Kelurahan Penatoi Kec. Mpunda Kota Bima Tahun 2023, program ini sudah dilaksanakan dan sudah berjalan sampai dengan saat ini.</p>



                                            <p>Tim Kesbangpol menyerap aspirasi dari beberapa warga yang ditemui, diantaranya :</p>



                                            <p>&#8211;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pembudidaya Keramba mengharapkan agar bantuan Bibit Ikan Lele yang diberikan Pemerintah tetap di laksanakan bahkan dengan harapan dapat ditingkatkan dan diberikan modal untuk membuat keramba serta dibantu untuk pemasaran hasil dari budidaya ikan tersebut.</p>



                                            <p>&#8211;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bapak Adhar (Exs Napiter), menginginkan bantuan agar mendapatkan bantuan perlengkapan untuk membuka usaha berjualan Es dan Jeruk Peras berupa Rombong, Alat Pemeras Jeruk beserta kelengkapan lainnya yang diperkirakan memerlukan biaya ± Rp. 4 Juta sd 5 Juta Rupiah, besar harapannya melalui kunjungan Pemda Provinsi maupun Kota Bima saat ini harapannya dapat terpenuhi.</p>



                                            <p>&#8211;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Bapak Rafiq (Exs. Napiter) yang sudah membuka usaha bengkel yang telah berjalan sampai saat ini, namun demikian masih sangat memerlukan perkakas bengkel lainnya berupa Kunci Set guna mendukung dan meningkatkan usaha perbengkelan yang sudah di rintis.</p>



                                            <p>Keinginan masyarakat akan ditampung dan diperhatikan serta disampaikan ke Pemerintah Daerah melalui Kesbangpol Kota Bima, dimana keinginan masyarakat tersebut akan disampaikan ke BNPT agar permasalahan masyarakat dapat ditampung dan dapat ditindaklanjuti.</p>



                                            <p>Pemda Provinsi dan Kota Bima akan selalu bersinergi dengan Fasilitator guna menampung permasalahan-permasalahan yang ada di masyarakat penatoi dan bersepakat akan selalu berkomunikasi dan melakukan pembinaan di kelurahan penatoi.</p>


